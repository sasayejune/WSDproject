const enlistData = [
    ...armyEnlistData,
    ...navyEnlistData,
    ...marineCorpsEnlistData,
    ...airForceEnlistData
];

function getQueryParameter(name) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(name);
}

function renderEnlistData(category) {
    const data = enlistData.filter(item => item.recruitField.includes(category));


    const outputTable = document.getElementById('table-body');
    outputTable.innerHTML = ""; // 기존 내용을 초기화

    if (!data || data.length === 0) {
        outputTable.innerHTML = `<p>희망 보직 관련하여 입영 신청 가능날짜가 없습니다. : ${category}</p>`;
        return;
    }

    // 데이터를 출력
    data.forEach(item => {
        const itemHTML = `
            <tr class="enlist-item" onclick="window.location.href = '${item.link}'">
                <th scope="row">1</th>
                <td>${item.recruitField}${item.detailField ? `(${item.detailField})` : ""}</td>
                <td>${item.applyPeriod}</td>
                <td>${item.enlistMonth}</td>
                <td>${item.recruitNumber}</td>
            </tr>
        `;
        outputTable.innerHTML += itemHTML;
    });
}

// 초기화 함수
function init() {
    const category = getQueryParameter("category");
    if (category) {
        renderEnlistData(category);
    } else {
        renderEnlistData("");
    }
}

// 페이지 로드 시 실행
window.onload = init;

// URL 변경 시 데이터 다시 로드 (이벤트 리스너)
window.onpopstate = init;

